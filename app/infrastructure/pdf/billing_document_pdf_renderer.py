def _make_footer_callback():
    """Return a canvas callback that draws 'Generated by Folio · Page X / Y'."""

    def _on_page(canvas, doc) -> None:  # type: ignore[no-untyped-def]
        canvas.saveState()
        canvas.setFont("DejaVu", 8)
        text = f"Generated by Folio · Page {doc.page}"
        page_width = A4[0]
        canvas.drawCentredString(page_width / 2, 8 * mm, text)
        canvas.restoreState()

    return _on_page


