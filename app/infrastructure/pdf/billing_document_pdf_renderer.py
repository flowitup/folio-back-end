def _fmt_pct(d) -> str:
    """Format a Decimal percentage without scientific notation: 10 → "10", 5.5 → "5.5"."""
    s = format(d, "f")
    # Strip trailing zeros after decimal point + dangling dot.
    if "." in s:
        s = s.rstrip("0").rstrip(".")
    return s or "0"


