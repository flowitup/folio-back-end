def _get_project_company_id(project_id: UUID) -> "UUID | None":
    """Return the company_id for a project, or None if not found / no company.

    Queries the ProjectModel directly so we can access company_id without
    extending the domain Project entity or IProjectRepository.
    """
    from app import db
    from app.infrastructure.database.models.project import ProjectModel

    row = db.session.get(ProjectModel, project_id)
    return row.company_id if row is not None else None


def _get_project_company_name(project_id: UUID) -> "str | None":
    """Return the legal_name of the company attached to a project, or None.

    Used to surface company identity on the invoices list response so the
    frontend can display which company is responsible for the expenses.
    """
    from app import db
    from app.infrastructure.database.models.company import CompanyModel

    company_id = _get_project_company_id(project_id)
    if company_id is None:
        return None
    company = db.session.get(CompanyModel, company_id)
    return company.legal_name if company is not None else None


def _get_company_payment_method_ids(project_id: UUID) -> "set[UUID]":
    """Return the set of payment_method IDs flagged is_company_payment for the project's company.

    Fetched in one query; ignores is_active so soft-deleted methods are included.
    Returns an empty set when the project has no company.
    """
    from app import db
    from app.infrastructure.database.models.payment_method import PaymentMethodModel

    company_id = _get_project_company_id(project_id)
    if company_id is None:
        return set()
    rows = (
        db.session.query(PaymentMethodModel.id)
        .filter(
            PaymentMethodModel.company_id == company_id,
            PaymentMethodModel.is_company_payment.is_(True),
        )
        .all()
    )
    return {r[0] for r in rows}


def _enrich_refunds_invoice_number(invoice_dict: dict, refunds_invoice_id_str: "str | None") -> None:
    """Inject refunds_invoice_number into an invoice dict in-place.

    When refunds_invoice_id is present, fetches the linked invoice's number in one
    db.session.get call so the FE can display "Refund of INV-2026-0001" without
    a separate round-trip.  Sets None when the link is absent or target not found.
    """
    if not refunds_invoice_id_str:
        invoice_dict["refunds_invoice_number"] = None
        return
    from app import db
    from uuid import UUID as _UUID

    linked = db.session.get(InvoiceModel, _UUID(refunds_invoice_id_str))
    invoice_dict["refunds_invoice_number"] = linked.invoice_number if linked is not None else None


def _enrich_has_bank_refund(invoice_dict: dict, invoice_id_str: str, invoice_type: str) -> None:
    """Inject has_bank_refund into an invoice dict in-place.

    True only when this materials_services expense has ≥1 linked refund invoice
    (i.e. a supplier/vendor sent money back — "refunded by bank"). The company
    signal rides on refundable_status == 'refunded'. False for all other types.
    """
    if invoice_type != InvoiceType.MATERIALS_SERVICES.value:
        invoice_dict["has_bank_refund"] = False
        return
    from uuid import UUID as _UUID

    sources = get_container().invoice_repository.refund_source_ids([_UUID(invoice_id_str)])
    invoice_dict["has_bank_refund"] = _UUID(invoice_id_str) in sources


def _enrich_invoice_with_company_payment(
    invoice_dict: dict,
    payment_method_id_str: "str | None",
    company_pm_id_strs: "set[str]",
) -> dict:
    """Inject paid_by_company into an invoice dict in-place and return it.

    paid_by_company is True only when the invoice has a payment_method_id that
    belongs to the project's company and is flagged is_company_payment.
    False when payment_method_id is null or the project has no company.
    """
    invoice_dict["paid_by_company"] = payment_method_id_str is not None and payment_method_id_str in company_pm_id_strs
    return invoice_dict


