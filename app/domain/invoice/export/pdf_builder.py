_FONTS_DIR = Path(__file__).resolve().parent.parent.parent / "labor" / "export" / "fonts"

# ---------------------------------------------------------------------------
# Font registration — executed at module import time
# ---------------------------------------------------------------------------
# Running at import eliminates the lazy-flag race condition where two concurrent
# requests could both observe _FONT_REGISTERED==False and double-register.
# Module-level code is serialised by the Python import system.
# A try/except guards against missing font files so import never hard-crashes;
# build_pdf() will fail (via ReportLab) if fonts are truly absent, surfacing
# the error at the right call site with a clear traceback.

try:
    pdfmetrics.registerFont(TTFont("DejaVu", str(_FONTS_DIR / "DejaVuSans.ttf")))
    pdfmetrics.registerFont(TTFont("DejaVu-Bold", str(_FONTS_DIR / "DejaVuSans-Bold.ttf")))
    pdfmetrics.registerFontFamily(
        "DejaVu",
        normal="DejaVu",
        bold="DejaVu-Bold",
        italic="DejaVu",
        boldItalic="DejaVu-Bold",
    )
except Exception as _font_err:  # noqa: BLE001
    import warnings

    warnings.warn(
        f"invoice pdf_builder: DejaVu font registration failed ({_font_err}). "
        "PDF generation will fail if fonts are unavailable.",
        stacklevel=1,
    )


