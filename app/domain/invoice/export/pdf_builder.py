def _make_styles() -> dict:
    """Return ParagraphStyle objects keyed by logical name (all DejaVu, never Helvetica)."""
    title = ParagraphStyle(
        "title",
        fontName="DejaVu-Bold",
        fontSize=24,
        leading=28,
        spaceAfter=2,
    )
    subtitle = ParagraphStyle(
        "subtitle",
        fontName="DejaVu",
        fontSize=11,
        leading=14,
        spaceAfter=4,
        textColor=colors.HexColor("#444444"),
    )
    meta = ParagraphStyle(
        "meta",
        fontName="DejaVu",
        fontSize=9,
        leading=12,
        textColor=colors.grey,
        spaceAfter=2,
    )
    section_heading = ParagraphStyle(
        "section_heading",
        fontName="DejaVu-Bold",
        fontSize=11,
        leading=14,
        spaceBefore=6,
        spaceAfter=2,
    )
    body = ParagraphStyle(
        "body",
        fontName="DejaVu",
        fontSize=9,
        leading=12,
    )
    body_italic = ParagraphStyle(
        "body_italic",
        fontName="DejaVu",
        fontSize=11,
        leading=14,
        textColor=colors.grey,
    )
    kpi_label = ParagraphStyle(
        "kpi_label",
        fontName="DejaVu",
        fontSize=7,
        leading=9,
        textColor=colors.grey,
        alignment=1,
    )
    kpi_value = ParagraphStyle(
        "kpi_value",
        fontName="DejaVu-Bold",
        fontSize=10,
        leading=13,
        alignment=1,
    )
    inv_header_left = ParagraphStyle(
        "inv_header_left",
        fontName="DejaVu-Bold",
        fontSize=13,
        leading=16,
    )
    inv_header_right = ParagraphStyle(
        "inv_header_right",
        fontName="DejaVu-Bold",
        fontSize=13,
        leading=16,
        alignment=2,
    )
    footer_style = ParagraphStyle(
        "footer_style",
        fontName="DejaVu",
        fontSize=8,
        leading=10,
    )
    return {
        "title": title,
        "subtitle": subtitle,
        "meta": meta,
        "section_heading": section_heading,
        "body": body,
        "body_italic": body_italic,
        "kpi_label": kpi_label,
        "kpi_value": kpi_value,
        "inv_header_left": inv_header_left,
        "inv_header_right": inv_header_right,
        "footer": footer_style,
    }


# ---------------------------------------------------------------------------
# Invoice sort key
# ---------------------------------------------------------------------------


def _invoice_sort_key(inv: Invoice):
    return (inv.issue_date, inv.type.value, inv.invoice_number)


# ---------------------------------------------------------------------------
# Page footer callback
# ---------------------------------------------------------------------------


def _make_footer_callback(context: InvoiceExportContext):
    """Return a canvas callback that draws footer on every page.

    Left:  "Generated by <email> · <ISO datetime>"
    Right: "Page X"
    (Page X only — two-pass X/Y total count is future work; mirrors labor precedent.)
    """
    iso_dt = context.generated_at.strftime("%Y-%m-%dT%H:%M")
    left_text = f"Generated by {context.generated_by_email} · {iso_dt}"

    def _footer(canvas, doc) -> None:  # type: ignore[no-untyped-def]
        canvas.saveState()
        canvas.setFont("DejaVu", 8)
        y = 8 * mm
        canvas.drawString(15 * mm, y, left_text)
        canvas.drawRightString(A4[0] - 15 * mm, y, f"Page {doc.page}")
        canvas.restoreState()

    return _footer


