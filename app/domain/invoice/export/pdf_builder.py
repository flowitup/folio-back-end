"""Pure-python PDF builder for invoice export.

build_pdf(context: InvoiceExportContext, bundle: InvoiceBundle) -> bytes

Layout (A4 portrait, 15mm margins)
------------------------------------
Page 1 — Summary:
  1. Header band — project name (24pt bold) + "INVOICE EXPORT" subtitle
  2. Meta line — range, generated datetime, generated_by email (9pt grey)
  3. KPI strip (4 cells) — Total invoices | Grand total | From | To
  4. "Subtotals by type" heading + table (Type | Count | Total)
  5. "Invoices" heading + table (# | Date | Type | Recipient | Total)

Pages 2..N+1 — one polished invoice per page:
  1. Header band: project name (left) + "INVOICE" (right) + bottom hairline
  2. Meta block (2-col table): Invoice # / Type / Issue date / Recipient (+ address)
  3. Items table: Description | Qty | Unit price | Total (zebra #fafafa rows)
  4. Grand-total band: full-width bold row, light grey background, top border
  5. Notes section (only if notes non-empty)

Page footer (every page):
  Left:  "Generated by <email> · <ISO datetime>"
  Right: "Page X"

Empty range: page 1 only, paragraph "No invoices in range … to …" replaces KPI/tables.

Currency rule (LOCKED)
-----------------------
All monetary values formatted via format_eur_fr(Decimal) → "200,00 €"
(matches xlsx number_format + FE Intl.NumberFormat fr-FR).

Font reuse note
---------------
This module deliberately reuses fonts from app.domain.labor.export.fonts via
the cross-package path below. This avoids duplicating ~1.5 MB of TTF binaries.
Accepted trade-off: if the labor package moves, update _FONTS_DIR here.
  _FONTS_DIR = Path(__file__).resolve().parent.parent.parent / "labor" / "export" / "fonts"

XML escaping
------------
All user-supplied strings (project name, recipient name/address, notes, item
descriptions) are passed through xml.sax.saxutils.escape before being rendered
in ReportLab Paragraph objects. Mirrors the labor PDF builder pattern.
"""

from __future__ import annotations

