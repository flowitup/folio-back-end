def _render_invoice_page(inv: Invoice, context: InvoiceExportContext, styles: dict, usable_width: float) -> list:
    """Build all story elements for a single polished invoice page."""
    elements: list = []
    type_label = TYPE_LABEL_EN.get(inv.type.value, inv.type.value.title())

    # --- Header band: project name (left) + "INVOICE" (right) + hairline ---
    header_data = [
        [
            Paragraph(_xml_escape(context.project_name), styles["inv_header_left"]),
            Paragraph("INVOICE", styles["inv_header_right"]),
        ]
    ]
    header_table = Table(header_data, colWidths=[usable_width * 0.6, usable_width * 0.4])
    header_table.setStyle(
        TableStyle(
            [
                ("LINEBELOW", (0, 0), (-1, -1), 0.75, colors.black),
                ("TOPPADDING", (0, 0), (-1, -1), 0),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
                ("LEFTPADDING", (0, 0), (-1, -1), 0),
                ("RIGHTPADDING", (0, 0), (-1, -1), 0),
                ("VALIGN", (0, 0), (-1, -1), "BOTTOM"),
            ]
        )
    )
    elements.append(header_table)
    elements.append(Spacer(1, 4 * mm))

    # --- Meta block: 2-col table ---
    recipient_lines = _xml_escape(inv.recipient_name)
    if inv.recipient_address:
        recipient_lines += "<br/>" + _xml_escape(inv.recipient_address)

    meta_rows = [
        ["Invoice #", _xml_escape(inv.invoice_number)],
        ["Type", type_label],
        ["Issue date", inv.issue_date.strftime("%Y-%m-%d")],
        ["Recipient", recipient_lines],
    ]

    meta_para_rows = []
    for label, value in meta_rows:
        label_para = Paragraph(f"<b>{label}</b>", ParagraphStyle("ml", fontName="DejaVu-Bold", fontSize=9, leading=12))
        value_para = Paragraph(value, ParagraphStyle("mv", fontName="DejaVu", fontSize=9, leading=12))
        meta_para_rows.append([label_para, value_para])

    meta_table = Table(meta_para_rows, colWidths=[usable_width * 0.25, usable_width * 0.75])
    meta_table.setStyle(
        TableStyle(
            [
                ("TOPPADDING", (0, 0), (-1, -1), 2),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 2),
                ("LEFTPADDING", (0, 0), (-1, -1), 0),
                ("RIGHTPADDING", (0, 0), (-1, -1), 4),
                ("VALIGN", (0, 0), (-1, -1), "TOP"),
            ]
        )
    )
    elements.append(meta_table)
    elements.append(Spacer(1, 4 * mm))

    # --- Items table: Description | Qty | Unit price | Total (zebra) ---
    items_header = ["Description", "Qty", "Unit price", "Total"]
    item_col_widths = [usable_width * 0.50, usable_width * 0.10, usable_width * 0.20, usable_width * 0.20]

    items_data = [items_header]
    for item in inv.items:
        items_data.append(
            [
                _xml_escape(item.description),
                str(item.quantity),
                format_eur_fr(item.unit_price),
                format_eur_fr(item.total),
            ]
        )

    items_style_cmds = [
        ("BACKGROUND", (0, 0), (-1, 0), colors.lightgrey),
        ("FONTNAME", (0, 0), (-1, 0), "DejaVu-Bold"),
        ("FONTSIZE", (0, 0), (-1, 0), 8),
        ("FONTNAME", (0, 1), (-1, -1), "DejaVu"),
        ("FONTSIZE", (0, 1), (-1, -1), 8),
        ("BOX", (0, 0), (-1, -1), 0.5, colors.black),
        ("INNERGRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),
        ("TOPPADDING", (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
        ("LEFTPADDING", (0, 0), (-1, -1), 4),
        ("RIGHTPADDING", (0, 0), (-1, -1), 4),
        ("ALIGN", (0, 0), (0, -1), "LEFT"),
        ("ALIGN", (1, 0), (-1, -1), "RIGHT"),
        ("ALIGN", (1, 0), (-1, 0), "CENTER"),
    ]

    # Zebra rows (#fafafa)
    for i in range(len(inv.items)):
        if i % 2 == 1:
            items_style_cmds.append(("BACKGROUND", (0, i + 1), (-1, i + 1), colors.HexColor("#fafafa")))

    items_table = Table(items_data, colWidths=item_col_widths)
    items_table.setStyle(TableStyle(items_style_cmds))
    elements.append(items_table)
    elements.append(Spacer(1, 2 * mm))

    # --- Grand-total band: full-width bold, light grey bg, top border ---
    grand_total_data = [["", "", "TOTAL", format_eur_fr(inv.total_amount)]]
    grand_table = Table(grand_total_data, colWidths=item_col_widths)
    grand_table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, -1), colors.HexColor("#E8F0FE")),
                ("LINEABOVE", (0, 0), (-1, 0), 1.0, colors.black),
                ("FONTNAME", (0, 0), (-1, -1), "DejaVu-Bold"),
                ("FONTSIZE", (0, 0), (-1, -1), 9),
                ("TOPPADDING", (0, 0), (-1, -1), 4),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
                ("LEFTPADDING", (0, 0), (-1, -1), 4),
                ("RIGHTPADDING", (0, 0), (-1, -1), 4),
                ("ALIGN", (2, 0), (2, 0), "RIGHT"),
                ("ALIGN", (3, 0), (3, 0), "RIGHT"),
            ]
        )
    )
    elements.append(grand_table)

    # --- Notes section (only if non-empty) ---
    if inv.notes:
        elements.append(Spacer(1, 4 * mm))
        notes_data = [
            [Paragraph("<b>Notes</b>", ParagraphStyle("nl", fontName="DejaVu-Bold", fontSize=9, leading=12))],
            [Paragraph(_xml_escape(inv.notes), ParagraphStyle("nb", fontName="DejaVu", fontSize=9, leading=12))],
        ]
        notes_table = Table(notes_data, colWidths=[usable_width])
        notes_table.setStyle(
            TableStyle(
                [
                    ("LINEABOVE", (0, 0), (-1, 0), 0.5, colors.lightgrey),
                    ("TOPPADDING", (0, 0), (-1, -1), 3),
                    ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
                    ("LEFTPADDING", (0, 0), (-1, -1), 0),
                    ("RIGHTPADDING", (0, 0), (-1, -1), 0),
                ]
            )
        )
        elements.append(notes_table)

    return elements


