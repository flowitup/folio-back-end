def _render_summary_header(context: InvoiceExportContext, styles: dict) -> list:
    """Project name (24pt bold) + 'INVOICE EXPORT' subtitle + meta line."""
    from_label = context.range.from_month.strftime("%Y-%m")
    to_label = context.range.to_month.strftime("%Y-%m")
    meta_text = (
        f"Range: {from_label} to {to_label}"
        f" · Generated {context.generated_at.strftime('%Y-%m-%d %H:%M')}"
        f" by {_xml_escape(context.generated_by_email)}"
    )
    return [
        Paragraph(_xml_escape(context.project_name), styles["title"]),
        Paragraph("INVOICE EXPORT", styles["subtitle"]),
        Paragraph(meta_text, styles["meta"]),
    ]


def _render_kpi_strip(bundle: InvoiceBundle, context: InvoiceExportContext, styles: dict) -> list:
    """4-cell KPI strip: Total invoices | Grand total | From | To."""
    from_label = context.range.from_month.strftime("%Y-%m")
    to_label = context.range.to_month.strftime("%Y-%m")

    kpi_data = [
        [
            Paragraph("Total invoices", styles["kpi_label"]),
            Paragraph("Grand total", styles["kpi_label"]),
            Paragraph("From", styles["kpi_label"]),
            Paragraph("To", styles["kpi_label"]),
        ],
        [
            Paragraph(str(bundle.invoice_count), styles["kpi_value"]),
            Paragraph(format_eur_fr(bundle.grand_total), styles["kpi_value"]),
            Paragraph(from_label, styles["kpi_value"]),
            Paragraph(to_label, styles["kpi_value"]),
        ],
    ]

    table = Table(kpi_data, colWidths="*")
    table.setStyle(
        TableStyle(
            [
                ("BOX", (0, 0), (-1, -1), 0.5, colors.lightgrey),
                ("INNERGRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),
                ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#F0F4FA")),
                ("TOPPADDING", (0, 0), (-1, -1), 4),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
                ("ALIGN", (0, 0), (-1, -1), "CENTER"),
                ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
            ]
        )
    )
    return [table]


def _render_subtotals_table(bundle: InvoiceBundle, styles: dict, usable_width: float) -> list:
    """'Subtotals by type' heading + table (Type | Count | Total)."""
    col_widths = [usable_width * 0.4, usable_width * 0.2, usable_width * 0.4]

    header_row = ["Type", "Count", "Total"]
    table_data = [header_row]
    for sub in bundle.subtotals_by_type:
        type_label = TYPE_LABEL_EN.get(sub.type.value, sub.type.value.title())
        table_data.append([type_label, str(sub.invoice_count), format_eur_fr(sub.total_amount)])

    style_cmds = [
        ("BACKGROUND", (0, 0), (-1, 0), colors.lightgrey),
        ("FONTNAME", (0, 0), (-1, 0), "DejaVu-Bold"),
        ("FONTSIZE", (0, 0), (-1, 0), 8),
        ("FONTNAME", (0, 1), (-1, -1), "DejaVu"),
        ("FONTSIZE", (0, 1), (-1, -1), 8),
        ("BOX", (0, 0), (-1, -1), 0.5, colors.black),
        ("INNERGRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),
        ("TOPPADDING", (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
        ("LEFTPADDING", (0, 0), (-1, -1), 4),
        ("RIGHTPADDING", (0, 0), (-1, -1), 4),
        ("ALIGN", (0, 0), (0, -1), "LEFT"),
        ("ALIGN", (1, 0), (-1, -1), "RIGHT"),
        ("ALIGN", (1, 0), (-1, 0), "CENTER"),
    ]

    table = Table(table_data, colWidths=col_widths)
    table.setStyle(TableStyle(style_cmds))
    return [Paragraph("Subtotals by type", styles["section_heading"]), table]


def _render_invoices_table(invoices: List[Invoice], styles: dict, usable_width: float) -> list:
    """'Invoices' heading + table (# | Date | Type | Recipient | Total)."""
    col_weights = [0.05, 0.12, 0.12, 0.51, 0.20]
    col_widths = [usable_width * w for w in col_weights]

    header_row = ["#", "Date", "Type", "Recipient", "Total"]
    table_data = [header_row]

    sorted_invoices = sorted(invoices, key=_invoice_sort_key)
    for idx, inv in enumerate(sorted_invoices, start=1):
        type_label = TYPE_LABEL_EN.get(inv.type.value, inv.type.value.title())
        table_data.append(
            [
                str(idx),
                inv.issue_date.strftime("%Y-%m-%d"),
                type_label,
                _xml_escape(inv.recipient_name),
                format_eur_fr(inv.total_amount),
            ]
        )

    style_cmds = [
        ("BACKGROUND", (0, 0), (-1, 0), colors.lightgrey),
        ("FONTNAME", (0, 0), (-1, 0), "DejaVu-Bold"),
        ("FONTSIZE", (0, 0), (-1, 0), 8),
        ("FONTNAME", (0, 1), (-1, -1), "DejaVu"),
        ("FONTSIZE", (0, 1), (-1, -1), 8),
        ("BOX", (0, 0), (-1, -1), 0.5, colors.black),
        ("INNERGRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),
        ("TOPPADDING", (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
        ("LEFTPADDING", (0, 0), (-1, -1), 4),
        ("RIGHTPADDING", (0, 0), (-1, -1), 4),
        ("ALIGN", (0, 0), (0, -1), "CENTER"),
        ("ALIGN", (1, 0), (2, -1), "CENTER"),
        ("ALIGN", (3, 0), (3, -1), "LEFT"),
        ("ALIGN", (4, 0), (4, -1), "RIGHT"),
        ("ALIGN", (1, 0), (4, 0), "CENTER"),
    ]

    # Alternate row shading
    for i in range(len(sorted_invoices)):
        if i % 2 == 1:
            style_cmds.append(("BACKGROUND", (0, i + 1), (-1, i + 1), colors.HexColor("#F7F9FC")))

    table = Table(table_data, colWidths=col_widths, repeatRows=1)
    table.setStyle(TableStyle(style_cmds))
    return [Paragraph("Invoices", styles["section_heading"]), table]


