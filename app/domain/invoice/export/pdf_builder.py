def build_pdf(context: InvoiceExportContext, bundle: InvoiceBundle) -> bytes:
    """Generate a PDF document for invoice export and return raw bytes.

    Args:
        context: Export metadata (project, range, generated_at, user email).
        bundle:  Sorted invoices + subtotals + grand_total.

    Returns:
        Valid PDF bytes beginning with b"%PDF-".

    Page 1 — Summary (header band, meta, KPI strip, subtotals table, invoices table).
    Pages 2..N+1 — One polished invoice per page.

    Empty-range behaviour:
        If bundle.invoice_count == 0, page 1 only with "No invoices in range" paragraph.
    """
    buf = BytesIO()
    margin = 15 * mm
    doc = SimpleDocTemplate(
        buf,
        pagesize=A4,
        leftMargin=margin,
        rightMargin=margin,
        topMargin=margin,
        bottomMargin=20 * mm,  # leave room for footer
    )

    usable_width = A4[0] - 2 * margin
    styles = _make_styles()
    footer_cb = _make_footer_callback(context)

    story: list = []

    # --- Page 1: Summary ---
    story.extend(_render_summary_header(context, styles))
    story.append(Spacer(1, 4 * mm))

    if bundle.invoice_count == 0:
        from_label = context.range.from_month.strftime("%Y-%m")
        to_label = context.range.to_month.strftime("%Y-%m")
        story.append(
            Paragraph(
                f"No invoices in range {from_label} to {to_label}",
                styles["body_italic"],
            )
        )
        doc.build(story, onFirstPage=footer_cb, onLaterPages=footer_cb)
        return buf.getvalue()

    story.extend(_render_kpi_strip(bundle, context, styles))
    story.append(Spacer(1, 4 * mm))
    story.extend(_render_subtotals_table(bundle, styles, usable_width))
    story.append(Spacer(1, 4 * mm))
    story.extend(_render_invoices_table(bundle.invoices, styles, usable_width))

    # --- Pages 2..N+1: one per invoice ---
    sorted_invoices = sorted(bundle.invoices, key=_invoice_sort_key)
    for inv in sorted_invoices:
        story.append(PageBreak())
        story.extend(_render_invoice_page(inv, context, styles, usable_width))

    doc.build(story, onFirstPage=footer_cb, onLaterPages=footer_cb)
    return buf.getvalue()
